//Area of Rhombus= (A X B)/2

#include<stdio.h>

void main()
{
    int A,B,tamp;
    printf("Enter the value of A : ");
    scanf("%d",&A);
    printf("Enter the value of B : ");
    scanf("%d",&B);

    tamp=(A*B)/2;

    printf("Enter the value of Area of Rhombus : %d ",tamp);    
}